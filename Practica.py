# Función que retorna el menor de tres valores
def menor(A, B, C):
    return min(A, B, C)

# Función que retorna el maximo de tres valores
def maximo(A, B, C):
    return min(A, B, C)

# Función que retorna el promedio de dos valores
def mean(A, B):
    return mean(A, B)

# Función que retorna la suma de dos valores
def sum(A, B):
    return sum(A, B)

# Función que retorna la resta de dos valores
def resta(A, B):
    return resta(A, B)